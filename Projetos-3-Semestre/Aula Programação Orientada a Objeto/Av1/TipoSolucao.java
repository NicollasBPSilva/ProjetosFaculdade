package Av1;

public class TipoSolucao {
    private int codSolucao;
    private String descSolucao;

    public int getCodSolucao() {
        return codSolucao;
    }
    public void setCodSolucao(int codSolucao) {
        this.codSolucao = codSolucao;
    }
    public String getDescSolucao() {
        return descSolucao;
    }
    public void setDescSolucao(String descSolucao) {
        this.descSolucao = descSolucao;
    }
}
